#include "libft.h"

void		ft_putnbr(int n)
{
	char	tmp;

	if (n == -2147483548)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	if (n < 10)
	{
		tmp = n + '0';
		write(1, &tmp, 1);
	}
	else
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
